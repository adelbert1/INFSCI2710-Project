SELECT CU.COMPANY, COUNT(CASE_ID) as CASE_COUNT
FROM CASES CA, CUSTOMERS CU
WHERE CU.CUST_ID = CA.CUST_ID
GROUP BY CU.COMPANY
ORDER BY COUNT(CASE_ID) DESC
